        ‘Diwata’ warns of siltation in Palawan coastline
<img src="/pictures/diwata.jpg" style="margin-top: 40px; height:400px; width:600px; "></div>

        <div class="box3"><br>
<p>An image taken by the Diwata-1 microsatellite shows areas in Brooke’s Point and Sofronio Española towns in Palawan where traces of siltation are found. —PHOTO COURTESY OF PHL MICROSAT
The country’s “eye in the sky” is warning of possible siltation in Palawan province, touted as the Philippine’s last ecological frontier, after capturing what appeared to be suspended sediments in two of its coastal areas.
The latest report by the PHL Microsat program showed that Diwata-1, the Philippine’s first microsatellite, was able to capture a series of images of Palawan through its space-borne multispectral imager (SMI) in December last year.
It noted that being a Unesco Biosphere Reserve and a protected area under the National Integrated Protected Areas System Act of 1992, “monitoring developments in Palawan has always been a priority.”
ADVERTISEMENT
“By harnessing Diwata’s capability of capturing images in the visible and near-infrared regions, [its] SMI could provide remotely sensed information on some of the current environmental issues in Palawan,” it said.
In one of Diwata’s snapshots of the province, “turbid sediment plumes” along the coastal waters off Brooke’s Point and Sofronio Española towns were “noticeable.”
The increased concentration of suspended sediments may be due to natural or human-induced soil erosion, according to the report.
Siltation is an environmental problem often caused by soil erosion or sediment spill. Among the harmful impacts of siltation include alteration of coastlines and changes in fish migration patterns. It may also damage the aquatic ecosystem.
While the images were part of an ongoing study that highlighted the many uses of Diwata-1, information gathered from the photographs should spur investigation and further study, according to Carlos Primo David, executive director of the Department of Science and Technology-Philippine Council for Industry, Energy and Emerging Research and Development (DOST-PCIEERD).
“More imaging will also be done,” David told the Inquirer on Monday.
The report noted that the watershed reserve in Palawan was vulnerable to soil erosion, especially when its forest cover had been reduced due to its topography—narrow mountain ranges, steep runoff slopes and meandering rivers.
It said the two towns have vast agricultural lands and agricultural activities, such as plowing and tilling methods, which could induce soil erosion.
The use of pesticides and fertilizers also aggravates the situation as these can be transported to the coasts through surface runoff, which can be detrimental to the marine ecosystem, according to the report.
“Moreover, it is interesting to further investigate whether the apparent siltation observed along the coast of Sofronio Española is indeed suspended sediments or just the shallower parts of the coast,” it said.
The report said Diwata-1, through its SMI, captured an image of a nickel mining site in the middle of a densely forested area in Sofronio Española. The snapshot showed that the mining site has “lower normalized difference vegetation index,” a measure for estimating vegetation health and density.
“Since river systems are agents of sediment transport toward the coastal areas, it is important to look into their tracks to monitor potential sources and points of contamination,” it said.
The PHL Microsat program is a collaboration between the DOST, University of the Philippines Diliman and Japan’s Hokkaido University. The program saw the launch of Diwata-1 in April last year, the first Filipino-built microsatellite. It aims to launch another satellite in the next two years.</p></div>

    
