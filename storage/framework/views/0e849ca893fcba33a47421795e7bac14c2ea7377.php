             Regions Briefs: BFAR Raises Red Tide Alert in Puerto Princesa
        </div>

        <div class="box4"><br>     
<p>Residents and visitors in Puerto Princesa City in Palawan province were advised against eating shellfish after the red tide alert was raised in the Puerto Princesa Bay. This brought to 12 the areas nationwide where harmful algal bloom was prevalent, according to the latest bulletin from the Bureau of Fisheries and Aquatic Resources (BFAR). BFAR said shellfish collected in these areas were found positive for paralytic shellfish poison beyond the regulatory limit. The agency said all kinds of shellfish and krill-like prawns (alamang) from an area under red tide alert should not be harvested, sold or eaten. However, fish, squid, shrimps and crabs are safe for human consumption provided these are fresh and washed thoroughly, and internal organs are removed before cooking. The problem results from the excessive blooming of harmful algae that produce toxins that are harmful to people and other marine animals, the BFAR said. Also on red tide alert are eight areas between the islands of Samar and Leyte, including coastal waters of Biliran province; Leyte province including Carigara Bay; Matarinao Bay in Eastern Samar; Maqueda, Villareal, Cambatutay and Irong-Irong Bays and Daram Island in Western Samar; Gigantes Islands in Carles, Iloilo; Dauis and Tagbilaran City in Bohol; and Balite Bay in Davao Oriental. —RONNEL W. DOMINGO</p>
