        <p>Let's now go to one of the most prominent places and active seaports in the history of the Philippines, Corregidor Island. This island is nestled in the southwestern part of Luzon Island, at the entrance of Manila Bay. (An island in a bay on an island! How cool is that?) It was the last island in the Philippines to be captured by the Japanese during WWII and is therefore a historic and sentimental place not only for the Filipinos and Japanese, but also for the US, who aided its liberation. You can still see remnants and relics of those battles, including the tunnels where Filipinos and Americans hid during the war and cannons they used to fight the Japanese ships and aircraft.
Some of the tourists that have been to the island have expressed feeling a lingering sense of those battles and say that they can feel the spirits of the soldiers who died during the war. In my experience, when I visited the island, I found myself thinking a lot about that history and imagining the pain and difficulty of life during WWII. So if you like vivid history, this is the place to go.
