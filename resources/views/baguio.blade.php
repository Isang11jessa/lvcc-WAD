       <p>Baguio City is one of the coolest places in the Philippines due to its high altitude. It is considered the summer capital of the Philippines because the best time to go there is summer, when the temperature in metro Manila reaches 37°C/98.6°F. Baguio City has many tourist spots: a botanical garden, Mines View Park, Wright Park where you can ride a horse, Burnham Park where you can ride a boat or a bike, and the mansion where the president of the Philippines stays when he visits. You can also try mountain climbing (Baguio is surrounded by beautiful mountains), and don't forget to buy some souvenirs because this city is known for its beautiful wooden crafts.
By the way, this is where you'll find the best halayang ube or purple yam jam in the entire world. And guess who makes it? Believe it or not, the nuns in Good Shepherd, a local convent, are the ones who make the best jam ever. They are also famous for their very delicious and sweet strawberry jam.
